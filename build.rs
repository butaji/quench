//! Build script for TuiBridge
//!
//! This script compiles JavaScript to QuickJS bytecode for production builds.

use std::env;
use std::fs;
use std::path::Path;

fn main() {
    let out_dir = env::var("OUT_DIR").unwrap();
    let dest_path = Path::new(&out_dir).join("bundle_qbc.rs");
    
    // Check if we have a bundle to compile
    let bundle_path = env::var("TUI_BRIDGE_BUNDLE")
        .unwrap_or_else(|_| "dist/bundle.js".to_string());
    
    if Path::new(&bundle_path).exists() {
        println!("cargo:rerun-if-changed={}", bundle_path);
        
        // Read the bundle
        let bundle = fs::read_to_string(&bundle_path).expect("Failed to read bundle");
        
        // Compile to bytecode using rquickjs
        // Note: This is a placeholder - actual bytecode compilation would require
        // calling the QuickJS compiler. In practice, you'd use the `qjs` CLI
        // or a proper build step.
        let bytecode = compile_to_bytecode(&bundle);
        
        // Generate Rust source that includes the bytecode
        let len = bytecode.len();
        
        let declaration = format!(
            r#"// Auto-generated by build.rs
use std::include_bytes;

pub static BUNDLE_BYTECODE: &[u8] = include_bytes!("../{bundle_path}");

pub fn get_bundle_size() -> usize {{
    {len}
}}
"#,
            bundle_path = bundle_path.replace('\\', "/"),
            len = len
        );
        
        fs::write(&dest_path, declaration).expect("Failed to write bundle_qbc.rs");
        println!("cargo::rustc-check-cfg=cfg(has_bytecode_bundle)");
    } else {
        // No bundle found - generate empty placeholder
        let declaration = r#"// No bundle - using runtime.js only
pub static BUNDLE_BYTECODE: &[u8] = &[];

pub fn get_bundle_size() -> usize {
    0
}
"#;
        fs::write(&dest_path, declaration).expect("Failed to write bundle_qbc.rs");
    }
    
    println!("cargo:rerun-if-changed=build.rs");
}

/// Compile JavaScript to QuickJS bytecode
/// 
/// In a real implementation, this would call the QuickJS compiler:
/// - Use the `qjs` CLI with `--compile` flag
/// - Or call `qjscc` (QuickJS C compiler)
/// - Or use a library like `rquickjs` with bytecode compilation
fn compile_to_bytecode(js_source: &str) -> Vec<u8> {
    // For now, we just return the source as bytes
    // A proper implementation would:
    // 1. Use `qjs --compile input.js -o output.qbc`
    // 2. Or use a Rust wrapper around the QuickJS compiler API
    
    // Placeholder: return UTF-8 bytes of the source
    // Real bytecode compilation is done externally via qjs CLI
    js_source.as_bytes().to_vec()
}
