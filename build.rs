//! Build script for Quench
//!
//! 1. Bundles JavaScript for optional deployment.
//! 2. Lints every Rust source file in the workspace against project rules:
//!    - File length: max 500 lines
//!    - Function length: max 40 lines
//!    - Cyclomatic complexity: max 10
//!
//! **Enforcement:** Panic on any violation. No `#[allow(...)]` exemptions.

use std::env;
use std::fs;
use std::path::{Path, PathBuf};

const MAX_FILE_LINES: usize = 500;
const MAX_FUNCTION_LINES: usize = 40;
const MAX_COMPLEXITY: usize = 10;

fn main() {
    generate_bytecode_bundle();
    run_linter();
}

fn generate_bytecode_bundle() {
    let out_dir = env::var("OUT_DIR").unwrap();
    let dest_path = Path::new(&out_dir).join("bundle_qbc.rs");
    let bundle_path = env::var("QUENCH_BUNDLE")
        .unwrap_or_else(|_| "dist/bundle.js".to_string());

    println!("cargo::rustc-check-cfg=cfg(has_bytecode_bundle)");
    println!("cargo:rerun-if-changed=build.rs");

    if !Path::new(&bundle_path).exists() {
        let declaration = "// No bundle - using runtime.js only\n\
            pub static BUNDLE_BYTECODE: &[u8] = &[];\n\n\
            pub fn get_bundle_size() -> usize { 0 }\n";
        fs::write(&dest_path, declaration).expect("Failed to write bundle_qbc.rs");
        return;
    }

    println!("cargo:rerun-if-changed={}", bundle_path);
    let bundle = fs::read_to_string(&bundle_path).expect("Failed to read bundle");
    let bytecode = bundle.as_bytes().to_vec();
    let len = bytecode.len();
    let declaration = format!(
        "// Auto-generated by build.rs\n\
         use std::include_bytes;\n\n\
         pub static BUNDLE_BYTECODE: &[u8] = include_bytes!(\"../{}\");\n\n\
         pub fn get_bundle_size() -> usize {{ {len} }}\n",
        bundle_path.replace('\\', "/")
    );
    fs::write(&dest_path, declaration).expect("Failed to write bundle_qbc.rs");
}

fn run_linter() {
    if let Err(e) = lint_all_rust_sources(Path::new(".")) {
        panic!("Lint violations:\n{e}");
    }
}

// ===================================================================
// Linter
// ===================================================================

#[derive(Debug)]
struct Violation {
    file: PathBuf,
    line: usize,
    kind: String,
    message: String,
}

fn lint_all_rust_sources(root: &Path) -> Result<(), String> {
    let mut violations = Vec::new();
    for path in collect_all_rs_files(root)? {
        let content = fs::read_to_string(&path).map_err(|e| e.to_string())?;
        let lines: Vec<&str> = content.lines().collect();
        check_file_length(&path, &lines, &mut violations);
        check_functions(&path, &lines, &mut violations);
    }
    if violations.is_empty() {
        Ok(())
    } else {
        Err(format_violations(&violations))
    }
}

fn collect_all_rs_files(root: &Path) -> Result<Vec<PathBuf>, String> {
    let mut files = Vec::new();
    collect_all_rs_files_rec(root, root, &mut files)?;
    Ok(files)
}

fn is_skipped_dir(path: &Path, root: &Path) -> bool {
    if path != root && path.join(".git").exists() {
        return true; // git submodule
    }
    path.components().any(|c| {
        let name = c.as_os_str().as_encoded_bytes();
        name == b".git"
            || name == b"target"
            || name == b"node_modules"
            || name == b"dist"
            || name == b"test262"
            || name == b"typescript"
    })
}

fn collect_all_rs_files_rec(dir: &Path, root: &Path, files: &mut Vec<PathBuf>) -> Result<(), String> {
    if is_skipped_dir(dir, root) {
        return Ok(());
    }
    let mut entries: Vec<_> = fs::read_dir(dir).map_err(|e| e.to_string())?.collect();
    entries.sort_by(|a, b| a.as_ref().unwrap().file_name().cmp(&b.as_ref().unwrap().file_name()));
    for entry in entries {
        let path = entry.map_err(|e| e.to_string())?.path();
        if path.is_dir() {
            collect_all_rs_files_rec(&path, root, files)?;
        } else if path.extension().and_then(|s| s.to_str()) == Some("rs") {
            files.push(path);
        }
    }
    Ok(())
}

fn check_file_length(path: &Path, lines: &[&str], violations: &mut Vec<Violation>) {
    if lines.len() > MAX_FILE_LINES {
        violations.push(Violation {
            file: path.to_path_buf(),
            line: lines.len(),
            kind: "file-length".to_string(),
            message: format!("file has {} lines (max {})", lines.len(), MAX_FILE_LINES),
        });
    }
}

fn format_violations(violations: &[Violation]) -> String {
    let mut msg = String::from("Lint violations found:\n");
    for v in violations {
        msg.push_str(&format!(
            "  [{}] {}:{} - {}\n",
            v.kind,
            v.file.display(),
            v.line,
            v.message
        ));
    }
    msg
}

// ===================================================================
// Function scanning
// ===================================================================

fn check_functions(path: &Path, lines: &[&str], violations: &mut Vec<Violation>) {
    let mut i = 0;
    while i < lines.len() {
        if !is_function_start(lines[i]) {
            i += 1;
            continue;
        }
        let Some((open_line, open_col)) = find_open_brace(lines, i) else {
            i += 1;
            continue;
        };
        let Some(close_line) = find_close_brace(lines, open_line, open_col) else {
            i = open_line + 1;
            continue;
        };
        if close_line > open_line + 1 {
            let body = &lines[open_line + 1..close_line];
            check_function_length(path, i + 1, body, violations);
            check_function_complexity(path, i + 1, body, violations);
        }
        i = close_line + 1;
    }
}

fn is_function_start(line: &str) -> bool {
    let t = line.trim_start();
    t.starts_with("fn ")
        || prefix_then_fn(t, "pub ")
        || prefix_then_fn(t, "unsafe ")
        || prefix_then_fn(t, "async ")
        || prefix_then_fn(t, "const ")
}

fn prefix_then_fn(trimmed: &str, prefix: &str) -> bool {
    trimmed.starts_with(prefix)
        && trimmed[prefix.len()..].trim_start().starts_with("fn ")
}

fn find_open_brace(lines: &[&str], start: usize) -> Option<(usize, usize)> {
    if let Some(c) = lines[start].find('{') {
        return Some((start, c));
    }
    for (j, line) in lines.iter().enumerate().skip(start + 1) {
        if let Some(c) = line.find('{') {
            return Some((j, c));
        }
    }
    None
}

fn find_close_brace(lines: &[&str], open_line: usize, open_col: usize) -> Option<usize> {
    let mut depth: isize = 1;
    for (j, line) in lines.iter().enumerate().skip(open_line) {
        let start = if j == open_line { open_col + 1 } else { 0 };
        if let Some(idx) = find_close_brace_in_line(line, start, &mut depth) {
            return Some(idx);
        }
    }
    None
}

fn find_close_brace_in_line(line: &str, start: usize, depth: &mut isize) -> Option<usize> {
    let mut in_string = false;
    let mut delim = '\0';
    let mut prev = '\0';
    let mut raw_hashes: usize = 0;

    for (idx, ch) in line.char_indices().skip(start) {
        if raw_hashes > 0 {
            if prev != '\\' && ch == '"' {
                let trailing = line.chars().skip(idx + 1);
                let n = trailing.take(raw_hashes).take_while(|c| *c == '#').count();
                if n == raw_hashes {
                    raw_hashes = 0;
                }
            }
            prev = ch;
            continue;
        }

        if in_string {
            if ch == delim && prev != '\\' {
                in_string = false;
            }
            prev = ch;
            continue;
        }

        if ch == '"' || ch == '\'' {
            in_string = true;
            delim = ch;
            prev = ch;
            continue;
        }

        if let Some(h) = raw_string_prefix_len(&line[idx..]) {
            raw_hashes = h;
            prev = ch;
            continue;
        }

        if prev == '\\' {
            prev = '\0';
            continue;
        }

        match ch {
            '{' => *depth += 1,
            '}' => {
                *depth -= 1;
                if *depth == 0 {
                    return Some(idx);
                }
            }
            _ => {}
        }
        prev = ch;
    }
    None
}

fn raw_string_prefix_len(s: &str) -> Option<usize> {
    let bytes = s.as_bytes();
    if bytes.is_empty() || bytes[0] != b'r' {
        return None;
    }
    let mut i = 1;
    while i < bytes.len() && bytes[i] == b'#' {
        i += 1;
    }
    if i < bytes.len() && bytes[i] == b'"' && i >= 1 {
        Some(i - 1)
    } else {
        None
    }
}

fn check_function_length(
    path: &Path,
    decl_line: usize,
    body: &[&str],
    violations: &mut Vec<Violation>,
) {
    if body.len() > MAX_FUNCTION_LINES {
        violations.push(Violation {
            file: path.to_path_buf(),
            line: decl_line,
            kind: "function-length".to_string(),
            message: format!("function body has {} lines (max {})", body.len(), MAX_FUNCTION_LINES),
        });
    }
}

fn check_function_complexity(
    path: &Path,
    decl_line: usize,
    body: &[&str],
    violations: &mut Vec<Violation>,
) {
    let complexity = check_cyclomatic(body);
    if complexity > MAX_COMPLEXITY {
        violations.push(Violation {
            file: path.to_path_buf(),
            line: decl_line,
            kind: "complexity".to_string(),
            message: format!("function complexity is {} (max {})", complexity, MAX_COMPLEXITY),
        });
    }
}

fn strip_comment(line: &str) -> &str {
    if line.starts_with("//") || line.starts_with("/*") || line.starts_with("*") {
        return "";
    }
    line.find("//").map(|idx| &line[..idx]).unwrap_or(line)
}

const DECISION_KW: &[&str] = &[" if ", " match ", " while ", " for ", " loop ", " else if "];
const DECISION_START: &[&str] = &["if ", "match ", "while ", "for ", "loop "];

fn count_decisions(code: &str) -> usize {
    let mut n = 0;
    for kw in DECISION_KW {
        n += code.matches(kw).count();
    }
    if DECISION_START.iter().any(|kw| code.starts_with(kw)) {
        n += 1;
    }
    n += code.matches(" && ").count();
    n += code.matches(" || ").count();
    n + code.matches('?').count()
}

fn check_cyclomatic(body: &[&str]) -> usize {
    let mut score = 1;
    for line in body {
        let code = strip_comment(line.trim());
        if !code.is_empty() {
            score += count_decisions(code);
        }
    }
    score
}
